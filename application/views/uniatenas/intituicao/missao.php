            <h1>Missão Institucional</h1>
            <p class="">
                O UniAtenas tem por missão contribuir para a construção de uma sociedade
                mais próspera, justa e solidária, promovendo uma educação transformadora, norteada
                por uma formação integral, humanística e técnico-profissional, alinhada a valores
                éticos e ao exercício da autonomia.
            </p>
            <p>
                A missão do UniAtenas não se restringe somente em formar um bom profissional
                com responsabilidade social, mas desenvolver o espírito crítico no aluno, tendo
                em vista que se entende por espírito crítico o trabalho de reflexão, que é uma espécie de
                voltar a si mesmo, analisando ou pondo em pauta os conhecimentos que possui, assim
                como levá-lo a refletir sobre o saber científico, interrogando o referido saber, em uma
                reflexão nutrida por informações precisas sobre este ou aquele domínio do real. Ao pensar
                em reflexão, insere-se a necessidade de procurar entender os mecanismos responsáveis pela própria reflexão.
            </p>


            <h2>Visão</h2>
            <p>
                Ser referência em educação de qualidade, inovadora nas propostas, nas práticas
                pedagógicas, no uso da tecnologia e líder de mercado na região em que atua.


            </p>				


            <h2>Valores
            </h2>

            a) <b>Amor pela Educação e pelo Trabalho:</b> Amamos o que fazemos, trabalhamos
            com prazer e sabemos da capacidade transformadora que a educação promove na
            sociedade.
            <br>
            b) <b>Respeito às Diferenças e a Justiça:</b> Respeitamos a diversidade, os direitos
            e a justiça, reconhecemos o valor de cada membro da comunidade acadêmica.
            <br>
            c) <b>Espírito de Equipe: </b>Sabemos que a união de pessoas trabalhando com cooperação,
            ética, responsabilidade, respeito e flexibilidade, focada nos mesmos objetivos,
            fortalece o trabalho para superação das metas com melhores resultados.
            <br>
            d) <b>Sustentabilidade: </b>Trabalhamos para consolidar e manter a instituição com
            excelente saúde econômica e financeira, assumindo o compromisso com a responsabilidade
            social e o respeito ao meio ambiente.
            <br>
            e)<b> Atitude de Dono:</b> Pensamos, falamos e agimos com comprometimento, como
            parte integrante da instituição.




